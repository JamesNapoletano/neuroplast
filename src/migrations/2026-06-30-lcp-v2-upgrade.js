const fs = require("fs");
const path = require("path");

const memory = require("../lcp/memory");
const { hashFileContents } = require("../cli/shared");

// Template bridge documents that a consumer may have edited (and so sync would
// preserve at v1.0). A major protocol upgrade legitimately rewrites these to the
// v2.0 shape; the prior content is recoverable from version control / backups.
const TEMPLATE_BRIDGE_FILES = [
  "manifest.yaml",
  path.join("profiles", "neuroplast-default.yaml"),
  path.join("workflows", "neuroplast-loop.yaml"),
  path.join("rules", "neuroplast-boundaries.yaml"),
  path.join("reasoning", "neuroplast-execution-scaffold.yaml"),
  path.join("tools", "neuroplast-cli.yaml")
];

// Top-level workflow instruction files (act/think) that carry the v2.0 assemble
// and quantize/distill steps. These were never given a managed baseline in 1.x,
// so the normal refresh cannot tell an untouched file from an edited one and
// defaults to preserving it — stranding upgraders on the v1 instructions. We
// upgrade a file only when its content still matches a known 1.x release hash
// (i.e. the consumer never edited it); genuinely edited copies are left alone.
const LEGACY_WORKFLOW_INSTRUCTIONS = [
  {
    source: ["src", "instructions", "act.md"],
    destination: ["neuroplast", "act.md"],
    // sha256 of act.md as shipped in 1.3.4 / 1.4.0 / 1.4.2 (identical across them).
    legacyHashes: ["16ec1ad47bea763abb8942f6bcc52edfd51108812d4ffe70891c7e1ce1b0c588"]
  },
  {
    source: ["src", "instructions", "think.md"],
    destination: ["neuroplast", "think.md"],
    // sha256 of think.md as shipped in 1.3.4 / 1.4.0 / 1.4.2 (identical across them).
    legacyHashes: ["2b9c3f32429c97d94545c953291dfd2243516d314007726e26a1035baaead1d0"]
  }
];

// LCP v2.0 upgrade. Template bridge documents (manifest, profile, rule, tool,
// workflow, reasoning) auto-upgrade through the normal managed-file refresh.
// This migration handles the two things that refresh cannot: replacing the
// now consumer-owned compatibility knowledge with its v2.0 shape, and migrating
// existing neuroplast/learning/*.md notes into the authoritative LCP learning
// memory artifact so durable memory follows the v2.0 memory model. The rendered
// learning view and quantized index are regenerated by the sync projection step
// that runs after migrations.
module.exports = {
  id: "2026-06-30-lcp-v2-upgrade",
  version: "2.0.0",
  description: "Upgrade the LCP bridge to v2.0 and migrate learning notes into LCP memory entries.",
  run(context) {
    const packageRoot = path.resolve(__dirname, "..", "..");
    const targetRoot = process.env.INIT_CWD || process.cwd();
    let scanned = 0;
    let updated = 0;

    ensureDirectory(path.join(targetRoot, ".lcp", "indexes"), context.dryRun);

    // Upgrade any template bridge document still on the v1.0 shape to v2.0.
    for (const fileName of TEMPLATE_BRIDGE_FILES) {
      scanned += 1;
      const source = path.join(packageRoot, "src", "lcp-files", fileName);
      const dest = path.join(targetRoot, ".lcp", fileName);
      const relative = ".lcp/" + fileName.split(path.sep).join("/");
      if (!fs.existsSync(source) || !fs.existsSync(dest)) {
        continue;
      }
      if (!isLegacyV1Document(dest)) {
        continue;
      }
      ensureDirectory(path.dirname(dest), context.dryRun);
      if (!context.dryRun) {
        fs.copyFileSync(source, dest);
      }
      if (typeof context.resetManagedBaseline === "function") {
        context.resetManagedBaseline(relative);
      }
      if (typeof context.logUpdatedFile === "function") {
        context.logUpdatedFile(relative);
      }
      updated += 1;
    }

    // Upgrade untouched top-level workflow instruction files (act/think) that
    // refresh preserves because 1.x never recorded a baseline for them.
    for (const item of LEGACY_WORKFLOW_INSTRUCTIONS) {
      scanned += 1;
      const source = path.join(packageRoot, ...item.source);
      const dest = path.join(targetRoot, ...item.destination);
      const relative = item.destination.join("/");
      if (!fs.existsSync(source) || !fs.existsSync(dest)) {
        continue;
      }
      if (typeof context.trackManagedFile === "function") {
        context.trackManagedFile(relative);
      }
      const currentHash = hashFileContents(fs.readFileSync(dest, "utf8"));
      const sourceHash = hashFileContents(fs.readFileSync(source, "utf8"));
      if (currentHash === sourceHash) {
        // Already on the v2.0 shape but never given a baseline; record one so future
        // upgrades flow through the normal 3-way managed refresh.
        if (!context.dryRun && typeof context.setManagedBaseline === "function") {
          context.setManagedBaseline(relative);
        }
        continue;
      }
      if (!item.legacyHashes.includes(currentHash)) {
        // Consumer edited this file; leave it for the refresh preserve path.
        continue;
      }
      if (!context.dryRun) {
        fs.copyFileSync(source, dest);
      }
      // Record the baseline from the just-written v2.0 content so the file is
      // controlled from this sync forward (prevents the no-baseline preserve bug
      // from recurring on the next upgrade).
      if (!context.dryRun && typeof context.setManagedBaseline === "function") {
        context.setManagedBaseline(relative);
      }
      if (typeof context.logUpdatedFile === "function") {
        context.logUpdatedFile(relative);
      }
      updated += 1;
    }

    // Replace the stale v1.0 compatibility knowledge with the v2.0 template.
    const compatSource = path.join(packageRoot, "src", "lcp-files", "knowledge", "neuroplast-compatibility.yaml");
    const compatDest = path.join(targetRoot, ".lcp", "knowledge", "neuroplast-compatibility.yaml");
    scanned += 1;
    if (fs.existsSync(compatSource)) {
      if (typeof context.trackManagedFile === "function") {
        context.trackManagedFile(".lcp/knowledge/neuroplast-compatibility.yaml");
      }
      if (needsCompatUpgrade(compatDest)) {
        ensureDirectory(path.dirname(compatDest), context.dryRun);
        if (!context.dryRun) {
          fs.copyFileSync(compatSource, compatDest);
        }
        if (typeof context.logCreatedFile === "function") {
          context.logCreatedFile(".lcp/knowledge/neuroplast-compatibility.yaml");
        }
        updated += 1;
      }
    }

    // Seed the learning memory artifact if it does not exist yet.
    const learningDest = path.join(targetRoot, ".lcp", "knowledge", "neuroplast-learning.yaml");
    const learningSource = path.join(packageRoot, "src", "lcp-files", "knowledge", "neuroplast-learning.yaml");
    if (!fs.existsSync(learningDest) && fs.existsSync(learningSource)) {
      ensureDirectory(path.dirname(learningDest), context.dryRun);
      if (!context.dryRun) {
        fs.copyFileSync(learningSource, learningDest);
      }
    }
    if (typeof context.trackManagedFile === "function") {
      context.trackManagedFile(".lcp/knowledge/neuroplast-learning.yaml");
    }

    // Migrate existing learning notes into LCP memory entries.
    const learningDir = path.join(targetRoot, "neuroplast", "learning");
    const artifact = fs.existsSync(learningDest)
      ? memory.loadArtifact(learningDest)
      : { lcp_version: "2.0", kind: "knowledge_artifact", id: "neuroplast-learning", title: "Neuroplast durable learning memory", content_type: "learning", entries: [], scope: { root: "." } };

    const existingIds = new Set((artifact.entries || []).map((entry) => entry && entry.id));
    let migratedCount = 0;

    if (fs.existsSync(learningDir)) {
      const noteFiles = fs.readdirSync(learningDir).filter((name) => name.endsWith(".md")).sort();
      for (const fileName of noteFiles) {
        scanned += 1;
        const slug = fileName.replace(/\.md$/, "");
        if (existingIds.has(slug)) {
          continue;
        }
        const absolute = path.join(learningDir, fileName);
        const note = memory.parseLearningNote(fs.readFileSync(absolute, "utf8"));
        const updatedAt = fileMtimeIso(absolute);
        const entry = memory.learningEntryFromNote(slug, note, { updatedAt });
        memory.upsertEntry(artifact, entry);
        existingIds.add(slug);
        migratedCount += 1;
      }
    }

    if (migratedCount > 0) {
      artifact.updated_at = new Date().toISOString().replace(/\.\d{3}Z$/, "Z");
      if (!context.dryRun) {
        memory.saveArtifact(learningDest, artifact);
      }
      if (typeof context.logCreatedFile === "function") {
        context.logCreatedFile(`.lcp/knowledge/neuroplast-learning.yaml (+${migratedCount} entries)`);
      }
      updated += migratedCount;
    }

    return { scanned, updated };
  }
};

function isLegacyV1Document(destPath) {
  try {
    return fs.readFileSync(destPath, "utf8").includes('lcp_version: "1.0"');
  } catch (error) {
    return false;
  }
}

function needsCompatUpgrade(destPath) {
  if (!fs.existsSync(destPath)) {
    return true;
  }
  const content = fs.readFileSync(destPath, "utf8");
  // Upgrade if still on the v1.0 shape (lcp_version 1.0 or legacy knowledge.statements).
  return content.includes('lcp_version: "1.0"') || content.includes("statements:");
}

function fileMtimeIso(absolutePath) {
  try {
    return fs.statSync(absolutePath).mtime.toISOString().replace(/\.\d{3}Z$/, "Z");
  } catch (error) {
    return new Date().toISOString().replace(/\.\d{3}Z$/, "Z");
  }
}

function ensureDirectory(directoryPath, dryRun) {
  if (fs.existsSync(directoryPath)) {
    return;
  }
  if (!dryRun) {
    fs.mkdirSync(directoryPath, { recursive: true });
  }
}
